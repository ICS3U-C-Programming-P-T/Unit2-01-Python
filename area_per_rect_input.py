# My Area & Perimeter of Rectangle, in Python


def main():
    print("For the rectangle with a length of 67cm and a width of 78cm")
    print("The area is {}cm^2".format(67 * 78))
    print("The perimeter is: {}cm2^".format(2 * (67 + 78)))


if __name__ == "__main__":
    main()
